const manager = AppDataSource.manager;

export class DataController {
  constructor(private bot: Bot) {
    this.bot.onMyData(this.myData.bind(this));
    this.bot.onDataCategory(this.dataCategory.bind(this));
    this.bot.onData(this.data.bind(this));
  }

  private async myData(user: User) {
    await this.bot.bot.sendMessage(
      +user.chatId,
      "–ó–¥–µ—Å—å —Ç—ã –º–æ–∂–µ—à—å –∑–∞–ø–æ–ª–Ω–∏—Ç—å –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—é –æ —Å–µ–±–µ, —Ç–µ–º —Å–∞–º—ã–º —É–ø—Ä–æ—Å—Ç–∏—Ç—å –ø–æ–ª—å–∑–æ–≤–∞–Ω–∏–µ –Ω–µ–π—Ä–æ-—Å–æ—Ç—Ä—É–¥–Ω–∏–∫–∞–º–∏, —Ç–∞–∫ –∫–∞–∫ –æ–Ω–∏ —É–∂–µ –±—É–¥—É—Ç –∑–∞—Ä–∞–Ω–µ–µ –æ–±–ª–∞–¥–∞—Ç—å –Ω–µ–æ–±—Ö–æ–¥–∏–º–æ–π –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏–µ–π. \n–¢–µ–º —Å–∞–º—ã–º —Ç—ã –ø–æ–ª—É—á–∞–µ—à—å –ª–∏—á–Ω—É—é –∫–æ–º–∞–Ω–¥—É –∞—Å—Å–∏—Å—Ç–µ–Ω—Ç–æ–≤, –∫–æ—Ç–æ—Ä—ã–µ –≤—Å–µ –∑–Ω–∞—é—Ç –æ —Ç–µ–±–µ –∏ –∫–∞–∫ —Å–ª–µ–¥—Å—Ç–≤–∏–µ —Ä–µ–∑—É–ª—å—Ç–∞—Ç—ã –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏—è —Å—Ç–∞–Ω—É—Ç –µ—â–µ –∫–æ–Ω—Å—Ç—Ä—É–∫—Ç–∏–≤–Ω–µ–µ –∏ –∫–æ–Ω–∫—Ä–µ—Ç–Ω–µ–µ!",
      {
        reply_markup: {
          inline_keyboard: [
            Btn("–û—Å–Ω–æ–≤–Ω–∞—è –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—è", "data-main"),
            Btn("–õ–∏—á–Ω–æ—Å—Ç—å –∏ –ø—Ä–µ–¥–ø–æ—á—Ç–µ–Ω–∏—è", "data-personal"),
            Btn("–†–∞–±–æ—Ç–∞ –∏ –∫–∞—Ä—å–µ—Ä–∞", "data-career"),
            Btn("–ë–∏–∑–Ω–µ—Å", "data-business"),
          ],
        },
      },
    );
  }

  private async dataCategory(user: User, category: string) {
    const cat = category as UserDataType;
    user.waitingForData = cat;
    await manager.save(user);
    await this.bot.bot.sendMessage(+user.chatId, map.get(cat)!);
  }

  private async data(user: User, text: string) {
    const key: UserDataTypeMapped = (user.waitingForData +
      "data") as UserDataTypeMapped;
    user[key] = text;
    user.waitingForData = "";
    await manager.save(user);
    await this.bot.bot.sendMessage(+user.chatId, "–î–∞–Ω–Ω—ã–µ —É—Å–ø–µ—à–Ω–æ –∑–∞–ø–æ–ª–Ω–µ–Ω—ã");
  }

  public async resetData(user: User) {
    user.dialogueData = '';
    user.waitingForData = '';
    user.waitingForPromo = false;

    user.agentId = null;
    user.agent = null;
    user.currentDialogId = null;
    user.currentAudioAgent = null;

    await manager.save(user);
  }

  private async takeFromData(user: User) {
    await this.bot.bot.sendMessage(+user.chatId, '–í—ã–±–µ—Ä–∏—Ç–µ —Ä–∞–∑–¥–µ–ª –¥–∞–Ω–Ω—ã—Ö:', {
      reply_markup: {
        inline_keyboard: [
          Btn('–û—Å–Ω–æ–≤–Ω—ã–µ', 'take-main'),
          Btn('–õ–∏—á–Ω–æ—Å—Ç–Ω—ã–µ', 'take-personal'),
          Btn('–ë–∏–∑–Ω–µ—Å', 'take-business'),
          Btn('–ö–∞—Ä—å–µ—Ä–∞', 'take-career')
        ]
      }
    })
  }
}
